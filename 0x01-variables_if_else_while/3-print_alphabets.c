#include <stdio.h>
/**
* main - main block
*
* Description: using the main function
* this program prints the alphabet in locase and then in upcase
* Return: 0
*/
int main(void)
{
	char c = 'a';

	while (c <= 'z')
	{
		putchar(c);
		c++;
	}
	c = 'A';

	while (c <= 'Z')
	{
		putchar(c);
		c++;
	}
	putchar('\n');
	return (0);
}
